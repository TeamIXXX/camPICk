package com.campick.dto;

public class CampgroundOptionDTO
{
	private String optionTypeName, optionTypeNum, optionName, optionNum;

	public String getOptionTypeName()
	{
		return optionTypeName;
	}

	public void setOptionTypeName(String optionTypeName)
	{
		this.optionTypeName = optionTypeName;
	}

	public String getOptionTypeNum()
	{
		return optionTypeNum;
	}

	public void setOptionTypeNum(String optionTypeNum)
	{
		this.optionTypeNum = optionTypeNum;
	}

	public String getOptionName()
	{
		return optionName;
	}

	public void setOptionName(String optionName)
	{
		this.optionName = optionName;
	}

	public String getOptionNum()
	{
		return optionNum;
	}

	public void setOptionNum(String optionNum)
	{
		this.optionNum = optionNum;
	}
	
}
