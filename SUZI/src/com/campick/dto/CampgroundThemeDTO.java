package com.campick.dto;

public class CampgroundThemeDTO
{
	private String themetypeNum, themetypeName, themeNum, themeName;

	public String getThemetypeNum()
	{
		return themetypeNum;
	}

	public void setThemetypeNum(String themetypeNum)
	{
		this.themetypeNum = themetypeNum;
	}

	public String getThemetypeName()
	{
		return themetypeName;
	}

	public void setThemetypeName(String themetypeName)
	{
		this.themetypeName = themetypeName;
	}

	public String getThemeNum()
	{
		return themeNum;
	}

	public void setThemeNum(String themeNum)
	{
		this.themeNum = themeNum;
	}

	public String getThemeName()
	{
		return themeName;
	}

	public void setThemeName(String themeName)
	{
		this.themeName = themeName;
	}
	
}
